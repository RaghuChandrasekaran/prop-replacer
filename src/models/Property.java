package models;

public class Property {
	private String key;
	private String udProperty;
	private String iwcProperty;
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getUdProperty() {
		return udProperty;
	}
	public void setUdProperty(String udProperty) {
		this.udProperty = udProperty;
	}
	public String getIwcProperty() {
		return iwcProperty;
	}
	public void setIwcProperty(String iwcProperty) {
		this.iwcProperty = iwcProperty;
	}
}
